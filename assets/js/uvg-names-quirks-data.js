export function nameGroupFor(id) {
  return UVG_NAME_GROUPS.find((group) => group.id === id) ?? null;
}

export function quirkTableFor(id) {
  return UVG_QUIRK_TABLES.find((table) => table.id === id) ?? null;
}

export function rollD6(random = Math.random) {
  return Math.floor(random() * 6) + 1;
}

export function quirkResultFor(tableId, roll) {
  const table = quirkTableFor(tableId);
  if (!table || !Number.isInteger(roll) || roll < 1 || roll > table.entries.length) {
    return null;
  }

  return {
    tableId,
    label: table.label,
    roll,
    text: table.entries[roll - 1],
  };
}
