export const UVG_HERO_COLUMNS = {
  identity: "Who are they?",
  reason: "Why are they on the road?",
  possession: "What do they bear?",
};

export function rollD50(random = Math.random) {
  return Math.floor(random() * 50) + 1;
}

export function heroResultFor(column, roll) {
  if (!(column in UVG_HERO_COLUMNS)) return null;
  if (!Number.isInteger(roll) || roll < 1 || roll > UVG_HERO_TABLE.length) return null;

  const row = UVG_HERO_TABLE[roll - 1];
  return {
    column,
    label: UVG_HERO_COLUMNS[column],
    roll,
    text: row[column],
  };
}

export function rollHero(random = Math.random) {
  return Object.keys(UVG_HERO_COLUMNS).reduce((results, column) => {
    const roll = rollD50(random);
    results[column] = heroResultFor(column, roll);
    return results;
  }, {});
}
